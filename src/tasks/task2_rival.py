"""
Task2: ライバルリサーチタスク
Agent2 (ライバルリサーチャー / Perplexity Sonar) が担当する。
定量データ（PER/PBR等）は Task2a で収集済み。本タスクは定性分析に集中する。
"""
from crewai import Task
from src.agents.agent2_rival import create_agent2


def create_task2(ticker: str, task2a: Task, company_name: str = "") -> Task:
    agent = create_agent2()
    return Task(
        description=(
            f"証券番号 {ticker} の企業について、以下の競合調査を実施してください:\n\n"
            "【前提】Task2a（競合データ収集）が各競合他社のPER・PBR・時価総額・配当利回りを\n"
            "すでに収集しています。その数値データをそのまま活用してください。\n\n"
            "【競合の最新動向・市場分析】\n"
            "1. 各競合他社の最新動向や市場シェア、各社の強み・弱みをウェブ検索で調べる\n"
            "2. 業界全体の市場規模・成長性・構造変化（技術トレンド・規制動向等）を調査する\n\n"
            "【競争優位性の具体的分析】\n"
            "3. 対象企業の競争優位性を以下の具体的視点で分析すること（抽象的な「技術力が高い」は不十分）:\n"
            "   - 顧客認証の障壁: 自動車・半導体向けの認定取得期間（何年かかるか）と認定コスト\n"
            "   - 製造難易度: 量産品質の再現に必要な設備・ノウハウの希少性\n"
            "   - スイッチングコスト: 顧客が競合製品に切り替えるためのコスト・リードタイム\n"
            "   - これらを踏まえ「なぜ競合に勝ち続けられるのか」を具体的に記述\n\n"
            "【カタリスト分析】\n"
            "4. 今後6〜18ヶ月で株価が動く可能性があるトリガー（カタリスト）を洗い出すこと:\n"
            "   - ポジティブカタリスト（例: 半導体市況回復・新製品受注・ROE改善施策の発表等）\n"
            "   - ネガティブカタリスト（例: EV化加速による主力製品の需要減・円高進展等）\n"
            "   - 各カタリストについて「発生確率（高/中/低）」と「株価インパクト（大/中/小）」も付記\n\n"
            "⚠️ 情報が取得できない場合は「該当データなし」と記載してスキップして構いません。"
        ),
        expected_output=(
            "【緩和条件】情報が完全に揃わなくても構いません。取得できない場合は「該当データなし」と記載して次に進んでください。\n"
            "対象企業と競合他社を比較した競争環境レポート。以下を含むこと:\n"
            "- Task2aで収集した競合PER・PBR数値テーブルをそのまま引用・整理したもの\n"
            "- 業界動向・市場シェアの定性分析\n"
            "- 競争優位性の源泉（顧客認証・製造難易度・スイッチングコストの具体的分析）\n"
            "- カタリスト一覧（ポジティブ/ネガティブ別、発生確率・株価インパクト付き）"
        ),
        agent=agent,
        context=[task2a],
    )
