"""
Task6: 総合レポート生成タスク（統括マネージャー担当）
Agent1〜5の結果を集約し、DCF法・マルチプル法で企業価値を算定して最終レポートを生成する。
"""
from crewai import Task
from src.agents.agent6_manager import create_agent6


def create_task6(
    ticker: str,
    task1: Task,
    task_gemini: Task,
    task2a: Task,
    task2b: Task,
    task3: Task,
    task4: Task,
    task5: Task,
    task_news: Task,
) -> Task:
    agent = create_agent6()
    return Task(
        description=(
            f"以下の各Agentの調査結果を統合し、証券番号 {ticker} の"
            "総合企業価値分析レポートを作成してください。\n\n"
            "【レポートに必ず含める項目】\n"
            "1. 企業概要・事業内容サマリー（Agent1の結果から）\n"
            "2. 業界構造・競争優位性の深い分析（Task2b（競合分析レポート）の結果から）\n"
            "   - 5フォース分析（既存競合/新規参入/代替品/買い手/売り手）\n"
            "   - 競争優位性の源泉（顧客認証の障壁・製造難易度・スイッチングコストを具体的に記述）\n"
            "3. 最新決算財務指標の評価（Agent3の結果から）\n"
            "   - 財務指標に加えて、必ず「セグメント別の業績（構成比や利益率）」と「配当・配当性向」を含めること\n"
            "   - Agent3による「ROEのデュポン分解とROIC分析」を必ず含めること\n"
            "   - ROEの低下・低水準については、過剰資本・資本回転低下・収益性低下のどれが根本原因かを経営問題として明示すること\n"
            "4. 業績トレンドの評価（Agent4の結果から）\n"
            "   - 必ず「配当金および配当性向の推移」に関する評価を含めること\n"
            "   - Agent4が収集したセグメント別CAGRと構成比を記載すること\n"
            "5. 株価・需給の評価（Agent5の結果から）\n"
            "6. DCF法による理論株価（DCFValuationToolで計算）\n"
            "   - Agent4から: 過去5〜10年の【確定通期実績】に基づくFCFリスト（free_cash_flowsパラメータ）\n"
            "     ⚠️【単位】IRBankFinancialTableToolのcf.free_cfはすべて1円単位（例: 1000億円=100,000,000,000）。\n"
            "     そのまま free_cash_flows に渡すこと。億・万・百万など他単位への変換は一切不要・禁止。\n"
            "   - Agent3から: 最新の営業利益率、発行済株式数、純有利子負債、負債比率\n"
            "   - 成長率（revenue_cagr）: Agent4のセグメント別CAGRと構成比が揃っている場合は、"
            "     segment_names・segment_weights・segment_growth_rates パラメータに渡してセグメント別加重平均CAGRを使用すること。"
            "     揃わない場合は全社CAGRを使用し、その旨をレポートに明記すること。\n"
            "   - margin_drift: Agent4の「将来成長率前提テーブル」でセグメントミックス変化が利益率に影響すると示された場合、"
            "     その方向と規模感を基に設定すること（例: 低利益セグメント拡大なら-0.01〜-0.02、"
            "     高利益セグメント拡大なら+0.01〜+0.02、変化なしなら0.0）。"
            "     0以外を設定した場合はレポートの前提条件にその根拠を明記すること。\n"
            "   - ⚠️ DCFの入力（free_cash_flows）には、Agent3による四半期からの年間化推定値ではなく、必ずAgent4が収集した『確定した複数年実績』を使用すること。\n"
            "   - ⚠️ レポートには必ず、DCFValuationToolの結果に含まれる【projected_fcf_table_markdown】フィールドをそのまま貼り付けること。\n"
            "     このフィールドには正確なラベル（例: 1,034億円）が事前に計算されている。LLMが独自に円単位→兆億変換を行うことは禁止。\n"
            "   - ⚠️ EVおよび株主価値をレポートに記載する際は、ツール結果の enterprise_value_label・equity_value_label フィールド（例: '1兆197億円'）をそのまま使用すること。自分で円単位の数値を兆・億に変換してはならない。\n"
            "   - ⚠️【継続価値の記載について】ツール結果には2種類のフィールドがある:\n"
            "       ・terminal_value_undiscounted = 未割引TV（参考値のみ）← レポートに記載しない\n"
            "       ・pv_terminal_value / pv_terminal_value_label = 割引済みTV（EV計算に使用） ← これをレポートに記載\n"
            "     レポートに記載する「継続価値」は必ず pv_terminal_value_label を使用すること。\n"
            "     ev_composition フィールド（例: 'EV = 事業価値XX億円 + 継続価値PV XX億円 = XX億円'）で加算の整合性を確認すること。\n"
            "   - ⚠️ DCFValuationToolの結果に含まれる【感応度分析テーブル（sensitivity_table_markdown）】を\n"
            "     必ずレポートのセクション6-1にそのまま掲載すること（WACC±1%×g±0.5%の3×5マトリックス）。\n"
            "   - sensitivity_wacc_base・sensitivity_g_base フィールドでベースケース（WACC・g）を確認すること。\n"
            "7. マルチプル法による理論株価（MultiplesValuationToolで計算）\n"
            "   - PER法のみを使用すること（EV/EBITDA法・SOTP法は実施しない）。\n"
            "   - PER法: Agent2から競合他社のPER（peer_pers）、Agent3から対象企業のEPS（target_eps）\n"
            "   - PER法（セグメント別加重）: Agent2がセグメント別競合PERテーブルを収集した場合は、"
            "     segment_names・segment_weights・segment_median_pers パラメータも渡してセグメント加重PERを使用すること。"
            "     収集できなかった場合はpeer_persのみで計算し「全体中央値PER使用」と明記すること。\n"
            "   - ⚠️ 競合データが不足している場合、LLMの判断で勝手に数値を仮定することは厳禁。データがない場合はその旨を正直に記載すること。\n"
            "8. 現在株価との乖離率と総合評価\n"
            "   【必須ルール】乖離率・割高割安の判定は必ず ValuationComparisonTool で計算すること。\n"
            "   LLMが乖離率を自分で計算することは絶対に禁止。\n"
            "   - current_price: Agent5から取得した現在株価（円）\n"
            "   - dcf_intrinsic_price: DCFValuationToolの intrinsic_price_per_share\n"
            "   - multiples_per_price: MultiplesValuationToolの per_method.per_implied_price\n"
            "   ツール結果の judgment（割高/割安）と summary（例: '現在株価は理論値比 +22.9% 割高'）をそのまま使用すること。\n"
            "   両手法の判定が異なる場合はその背景・理由を考察すること。\n"
            "   - roic: Agent3のFinancialCalcToolで計算したROIC（例: 0.035）を渡すこと。\n"
            "   - base_wacc: DCFValuationToolの wacc 出力（例: 0.0396）を渡すこと。\n"
            "   - value_trap_warning が出力された場合は必ずセクション6-3および投資判断サマリーに引用し、\n"
            "     「買い」判断の場合は「ROIC<WACCの価値破壊状態であり、買い判断はROIC改善前提に依存する」\n"
            "     という留保条件を必ず明示すること。\n"
            "9. 主要リスク・課題（上位5件）: Agent1から提供されるリスク情報（JSON形式）をパースし、各リスクの影響度（大・中・小）とその判定根拠を明確に含めること。\n"
            "10. 投資シナリオ分析（3シナリオ）\n"
            "    - 強気シナリオ: 最もポジティブな前提（半導体回復・新事業成功・ROE改善等）と期待株価レンジ\n"
            "      ※ 強気シナリオには以下のROIC改善シナリオを含めること:\n"
            "         a) CCC（キャッシュコンバージョンサイクル）圧縮による運転資本効率改善\n"
            "            - Agent3が計算したCCC日数を業界平均（製造業: 60〜90日目安）と比較\n"
            "            - CCC改善余地がある場合: 圧縮によって解放される運転資本を試算し、ROIC改善を定性的に示すこと\n"
            "         b) 遊休資産売却・設備稼働率向上による投下資本削減\n"
            "         c) これらによりROICがWACCを上回る時期の見通し（定性コメント）\n"
            "    - 中立シナリオ: 現状維持の前提と期待株価レンジ\n"
            "    - 弱気シナリオ: 最もネガティブな前提（EV化加速・主力製品需要減・円高等）と期待株価レンジ\n"
            "    - ⚠️ 各シナリオの株価レンジはLLMが定性的に記載する（ToolによるDCF再計算は不要）\n"
            "11. 最新ニュース・市場動向（Gemini・Perplexity調査より）\n"
            "    ⚠️ このセクションは「現在進行中の事実」を記述する。未来予測・カタリスト（次項）とは厳密に区別すること。\n"
            "    a) 対象企業の最新動向（Agent_Geminiのリサーチから）\n"
            "       - 市場・投資家が現在最も注目しているテーマ（AI・フィジカルAI・DX等）: ★最新マーク付きで引用\n"
            "       - 直近の決算説明会・IR資料での経営陣の主要メッセージ\n"
            "       - 製品・技術の最新市場評価（大型受注・新製品発表・競合比較の変化）\n"
            "       - ESG・ガバナンス動向（アクティビスト・株主提案等）\n"
            "    b) 業界・競合の最新動向（Task2（Perplexity）およびTask2bの競合分析から）\n"
            "       - 業界で現在起きていること（市場シェア変動・技術トレンド・規制変化）\n"
            "       - 主要競合他社の直近の動き（新製品・受注・M&A・業績変化）\n"
            "       - 業界全体の需給バランスの現状\n"
            "    c) マクロ・地政学ニュース（Agent_newsから）\n"
            "       - 対象企業のセクターに直接影響するマクロニュース（関税・規制・為替・サプライチェーン等）\n"
            "       - 業界固有の地政学リスクの現状\n"
            "    【注意】過去12ヶ月以内の確認済み事実のみを記載。憶測・予測は禁止。情報源が不明な場合は「情報なし」と記載すること。\n"
            "12. カタリスト分析（Task2b（競合分析レポート）の結果から）\n"
            "    - ポジティブカタリスト・ネガティブカタリスト各3〜5件を発生確率・株価インパクト付きで記載\n"
            "13. 投資判断サマリー\n"
            "    【必須ルール】以下の構造で明確に表明すること:\n"
            "    a) 投資判断: 「買い」/「売り」/「中立」の3択から1つを明確に選択すること\n"
            "       ※ 両論を並べて結論を曖昧にすることは不可。現時点の見解を根拠付きで表明する\n"
            "    b) 定量根拠: 8.で算出したDCF乖離率・マルチプル乖離率（割高/割安の数値）を必ず引用すること\n"
            "    c) 定性根拠: 競争優位性・業績トレンド・カタリストのうち判断を支持する要因を2〜3点記載\n"
            "    d) 前提・限界: この判断が崩れる条件（例: 半導体市況急悪化で判断変化）を1〜2点明記\n"
            "    e) 免責: 本レポートは参考情報であり最終投資判断はユーザー自身の責任で行うことを1行で明記\n\n"
            "⚠️ Agent_news（業界・地政学ニュースレポート）のSTEP 5サマリーを必ず参照し、\n"
            "   リスク・課題（セクション7）とカタリスト分析（セクション10）に最新ニュース情報を反映すること。\n"
            "   特にAgent_newsが指摘する「見落とされがちなリスク」は、必ずセクション7に独立項目として記載すること。\n"
            "⚠️ 各Agentから取得したデータに欠落がある場合は、MarkdownReadTool を使って `data/{ticker}/kessan_tansin.md` を直接読み込み、生データから数値を補完すること。\n"
            "⚠️ 数値計算は必ず DCFValuationTool および MultiplesValuationTool で実行すること。\n"
            "⚠️ LLM自身は絶対に数値計算を行わないこと。"
        ),
        expected_output=(
            "【緩和条件】情報が完全に揃わなくても構いません。取得できない場合は「該当データなし」と記載して次に進み、情報の深追いや無限ループを避けてください。\n"
            "Markdown形式の完全な企業価値分析レポート（4000〜6000文字程度）。\n"
            "以下の見出し構造を含むこと:\n"
            "# 企業価値分析レポート: {企業名} ({ticker})\n"
            "## 1. 企業概要・事業内容\n"
            "## 2. 業界構造・競争環境分析\n"
            "   - 競争優位性は顧客認証・製造難易度・スイッチングコストの具体的記述を含むこと\n"
            "## 3. 最新決算財務指標（セグメント業績・ROE根本原因分析・配当方針）\n"
            "   - ROEとROICの比較、ROE低下の根本原因分析を含むこと\n"
            "## 4. 複数年業績推移（セグメント別CAGR・配当・配当性向のトレンドを含む）\n"
            "## 5. 株価・需給状況\n"
            "## 6. 企業価値算定\n"
            "### 6-1. DCF法による企業価値\n"
            "   - 前提条件（WACC、成長率等・セグメント別加重平均CAGRを使用した場合はその内訳）に加え、DCFValuationToolのprojected_fcf_table_markdownをそのまま貼り付けること。\n"
            "   - 継続価値（TV）の計算結果と、それが全体の企業価値に占める割合（%）も記載すること。\n"
            "   - 感応度分析テーブル（WACC×g マトリックス 3行×5列）を含むこと。\n"
            "### 6-2. マルチプル法（PER法）\n"
            "   - セグメント加重PERを使用した場合はその内訳（セグメント名/構成比/競合PER中央値）を記載すること\n"
            "### 6-3. 総合評価・現在株価との乖離\n"
            "   - 各手法の割高/割安判定（符号付き乖離率）を明確に記載すること\n"
            "## 7. リスク・課題\n"
            "   - リスク項目、影響度（大・中・小）、根拠・分析を含むテーブル形式で記述すること。\n"
            "## 8. 投資シナリオ（強気/中立/弱気）\n"
            "   - 各シナリオの前提条件・期待株価レンジ・確率評価を含むこと\n"
            "## 9. 最新ニュース・市場動向\n"
            "   - a) 対象企業の最新動向（Geminiリサーチより: 市場注目テーマ・IR動向・製品評価・ESG）\n"
            "   - b) 業界・競合の最新動向（Perplexity/Task2bより: 市場シェア・競合動向・業界需給）\n"
            "   - c) マクロ・地政学ニュース（Agent_newsより: セクター影響マクロニュース）\n"
            "   ※ 確認済み事実のみ。未来予測はカタリスト分析（セクション10）に記載すること\n"
            "## 10. カタリスト分析\n"
            "   - ポジティブ/ネガティブ別、発生確率・株価インパクト付きの一覧テーブル\n"
            "## 11. 投資判断サマリー\n"
            "   - 買い/売り/中立（3択）の明確な判断\n"
            "   - 定量根拠（DCF・マルチプル乖離率）+ 定性根拠（2〜3点）\n"
            "   - 判断が崩れる前提条件\n"
            "   - 免責事項（1行）"
        ),
        agent=agent,
        context=[task1, task_gemini, task2a, task2b, task3, task4, task5, task_news],  # 全Agentの結果を参照
    )
