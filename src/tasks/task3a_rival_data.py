"""
Task3a: 競合データ収集タスク
Agent3a (競合データ収集エージェント) が担当。
Task3 (Perplexity情報収集) が特定した競合他社リストをもとに、
StockScraperToolで各社の定量データを収集する。
"""
from crewai import Task
from src.agents.agent3a_rival_data import create_agent3a


def create_task3a(ticker: str, task3: Task) -> Task:
    agent = create_agent3a()
    return Task(
        description=(
            f"証券番号 {ticker} の企業の競合他社の財務指標を収集してください。\n\n"
            "【前提】Task3（Perplexity情報収集）がすでに主要競合他社と事業セグメント構造を特定しています。\n"
            "Task3のSection 2に記載された競合他社リスト（社名・証券番号）を優先して使用してください。\n\n"
            "【手順1: 収集目標社数の決定】\n"
            "Task3の出力から対象企業の主要セグメント数（N）を確認し、収集目標社数を決定すること:\n"
            "  - セグメント数 N=1（単一事業）: 合計 5〜7社\n"
            "  - セグメント数 N=2: 合計 6〜8社（各セグメント 3〜4社）\n"
            "  - セグメント数 N=3以上: 合計 N×3社（各セグメント 3社）\n"
            "  ※ セグメント境界が曖昧・分類困難な場合は N=1 として扱ってよい\n\n"
            "【手順2: 競合他社リストの確保】\n"
            f"Task3が特定した競合他社の証券番号を確認し、手順1で決定した目標社数に達するまで以下の手順で補完すること:\n"
            f"   a) KabutanThemeListTool(ticker='{ticker}') を呼び出して対象企業のテーマ一覧を取得する\n"
            "   b) 競合分析に有用なテーマ（業界・製品カテゴリ・技術等）を 1〜3件選ぶこと。\n"
            "      以下は除外すること:\n"
            "      ✗ 株価指数（TOPIX・JPX日経400・読売333 等）\n"
            "      ✗ マクロ・為替（円安メリット・ユーロ高 等）\n"
            "      ✗ 地域テーマ（中国関連・インド関連 等）\n"
            "      ✗ 投資スタイル（NISA関連・ESG投資・優待人気 等）\n"
            "      ✓ 選ぶべき例: 製品カテゴリ・業界名・技術領域（自動車・半導体・EV関連 等）\n"
            "   c) 選んだテーマURLそれぞれに対して KabutanThemeStocksTool(theme_url=...) を呼び出し、\n"
            "      銘柄コードと銘柄名の一覧を取得する\n"
            "      ※ URLは '/themes/?theme=...' の相対パスをそのまま渡してよい\n"
            "   d) 取得した銘柄リストから対象企業自身（証券番号 {ticker}）を除き、\n"
            "      セグメントに対応する競合候補を選定して証券番号リストに追加する\n\n"
            "【手順3: 財務データの一括取得】\n"
            "競合他社の証券番号リストが確定したら、以下のツールをそれぞれ1回ずつ呼び出すこと。\n"
            "tickers に証券番号のリストをまとめて渡せば、全社分のデータが一括取得できる。\n"
            "   ① KabutanBatchTool: PER・PBR・時価総額・配当利回りを一括取得\n"
            "   ② TradingViewEVEBITDATool: EV/EBITDA倍率を一括取得\n"
            "      ※ EV/EBITDAがマイナスの場合はTradingViewに表示されないことがある\n\n"
            "【手順4: セグメント別競合PERテーブルの作成】\n"
            "手順1でN≥2と判定した場合は、収集した競合社をセグメントに割り当てること:\n"
            "  - 各セグメントに最低3社を割り当て、セグメント名・競合社名・PER値・PER中央値をテーブルで示す\n"
            "    （例: 自動車セグメント=[日産13倍, ホンダ12倍, スズキ15倍] 中央値=13倍）\n"
            "  - これは後続Agent7のセグメント加重PER計算に使用される\n"
            "  ※ セグメントN=1または分類困難な場合は全体競合PERリストのみで可\n\n"
            "⚠️ データ取得に失敗した場合は「該当データなし」と記載してスキップして構いません。"
        ),
        expected_output=(
            "以下を含む競合他社データレポート:\n"
            "- 【収集目標】セグメント数Nと目標社数の判定結果（例: N=2 → 目標6〜8社）\n"
            "- 収集した競合他社全社のPER・PBR・時価総額・配当利回り・EV/EBITDAの具体的数値テーブル\n"
            "- （N≥2の場合）セグメント別競合PERテーブル（セグメント名/競合社名/PER値/中央値）\n"
            "  各セグメントに最低3社を割り当てること\n"
            "- データ取得できなかった企業・指標は「該当データなし」と明記"
        ),
        agent=agent,
        context=[task3],
    )
