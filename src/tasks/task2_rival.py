"""
Task2: ライバルリサーチタスク
Agent2 (ライバルリサーチャー) が担当する。
"""
from crewai import Task
from src.agents.agent2_rival import create_agent2


def create_task2(ticker: str, company_name: str = "") -> Task:
    agent = create_agent2()
    return Task(
        description=(
            f"証券番号 {ticker} の企業について、以下の競合調査を実施してください:\n\n"
            "【競合指標の収集】\n"
            "1. 主要な競合他社（国内・海外問わず）を3〜5社程度特定\n"
            "2. 競合他社の証券番号（4桁）を特定し、StockScraperTool(source='kabutan_stock')で各社の詳細ページを取得し、"
            "以下の指標を抽出すること:\n"
            "   - PER、PBR、時価総額、配当利回り\n"
            "   - EV/EBITDA（企業価値/EBITDA）: WebSearchToolで「{competitor} EV/EBITDA」等を検索して取得\n"
            "3. WebSearchToolを用いて、競合の最新動向や市場シェア、各社の強み・弱みを調べる\n"
            "3-補足. 対象企業が複数の異質セグメント（例: 自動車系と半導体系のように成長性が著しく異なるもの）を"
            "持つ場合、セグメントに対応する競合グループ別にPERを分けて収集すること:\n"
            "   - 各セグメントに対応する競合2〜3社のPERを収集し、セグメント名・競合社名・PER値・PER中央値を"
            "     テーブルで示すこと（例: 自動車セグメント=[日本特殊陶業13倍, NGK Spark Plug15倍] 中央値=14倍）\n"
            "   - これはAgent6のセグメント加重PER計算（MultiplesValuationToolのsegment_median_persパラメータ）に使用される\n"
            "   ※ セグメントが1つ、または分類困難な場合は従来通り全体競合PERリストのみで可\n\n"
            "【競争優位性の具体的分析】\n"
            "4. 対象企業の競争優位性を以下の具体的視点で分析すること（抽象的な「技術力が高い」は不十分）:\n"
            "   - 顧客認証の障壁: 自動車・半導体向けの認定取得期間（何年かかるか）と認定コスト\n"
            "   - 製造難易度: 量産品質の再現に必要な設備・ノウハウの希少性\n"
            "   - スイッチングコスト: 顧客が競合製品に切り替えるためのコスト・リードタイム\n"
            "   - これらを踏まえ「なぜ競合に勝ち続けられるのか」を具体的に記述\n\n"
            "【カタリスト分析】\n"
            "5. 今後6〜18ヶ月で株価が動く可能性があるトリガー（カタリスト）を洗い出すこと:\n"
            "   - ポジティブカタリスト（例: 半導体市況回復・新製品受注・ROE改善施策の発表等）\n"
            "   - ネガティブカタリスト（例: EV化加速による主力製品の需要減・円高進展等）\n"
            "   - 各カタリストについて「発生確率（高/中/低）」と「株価インパクト（大/中/小）」も付記\n\n"
            "⚠️ 情報が取得できない場合は「該当データなし」と記載してスキップして構いません。"
        ),
        expected_output=(
            "【緩和条件】情報が完全に揃わなくても構いません。取得できない場合は「該当データなし」と記載して次に進んでください。\n"
            "対象企業と競合他社を比較した競争環境レポート。以下を含むこと:\n"
            "- 各競合のPER・PBR・EV/EBITDAの具体的数値テーブル\n"
            "- （複数異質セグメントがある場合）セグメント別競合PERテーブル（セグメント名/競合社名/PER値/中央値）\n"
            "- 競争優位性の源泉（顧客認証・製造難易度・スイッチングコストの具体的分析）\n"
            "- カタリスト一覧（ポジティブ/ネガティブ別、発生確率・株価インパクト付き）"
        ),
        agent=agent,
    )
