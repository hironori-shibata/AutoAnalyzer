"""
Task6: 総合レポート生成タスク（統括マネージャー担当）
Agent1〜5の結果を集約し、DCF法・マルチプル法で企業価値を算定して最終レポートを生成する。
"""
from crewai import Task
from src.agents.agent6_manager import create_agent6


def create_task6(
    ticker: str,
    task1: Task,
    task2: Task,
    task3: Task,
    task4: Task,
    task5: Task,
) -> Task:
    agent = create_agent6()
    return Task(
        description=(
            f"以下の各Agentの調査結果を統合し、証券番号 {ticker} の"
            "総合企業価値分析レポートを作成してください。\n\n"
            "【レポートに必ず含める項目】\n"
            "1. 企業概要・事業内容サマリー（Agent1の結果から）\n"
            "2. 業界構造・競争優位性の深い分析（Agent2の結果から）\n"
            "   - 5フォース分析（既存競合/新規参入/代替品/買い手/売り手）\n"
            "   - 競争優位性の源泉\n"
            "3. 最新決算財務指標の評価（Agent3の結果から）\n"
            "   - 財務指標に加えて、必ず「セグメント別の業績（構成比や利益率）」と「配当・配当性向」を含めること\n"
            "   - Agent3による「ROEのデュポン分解（収益性・効率性・財務レバレッジ）」に基づいた構造的分析を必ず含めること\n"
            "4. 業績トレンドの評価（Agent4の結果から）\n"
            "   - 必ず「配当金および配当性向の推移」に関する評価を含めること\n"
            "5. 株価・需給の評価（Agent5の結果から）\n"
            "6. DCF法による理論株価（DCFValuationToolで計算）\n"
            "   - Agent4から: 過去5〜10年の【確定通期実績】に基づくFCFリスト（free_cash_flowsパラメータ）、売上CAGR\n"
            "   - Agent3から: 最新の営業利益率、発行済株式数、純有利子負債、負債比率\n"
            "   - ⚠️ DCFの入力（free_cash_flows）には、Agent3による四半期からの年間化推定値ではなく、必ずAgent4が収集した『確定した複数年実績』を使用すること。\n"
            "   - ⚠️ レポートには必ず、DCFValuationToolの結果に含まれる【将来5年間の予測FCFリスト】をテーブル形式で明示し、計算の透明性を確保すること。\n"
            "7. マルチプル法による理論株価（MultiplesValuationToolで計算）\n"
            "   - Agent2から: 競合他社のPER（peer_pers）\n"
            "   - Agent3から: 対象企業のEPS（target_eps）\n"
            "   - ⚠️ 競合データが不足している場合、LLMの判断で勝手に数値を仮定（例：10.0倍とする等）することは厳禁。データがない場合はその旨を正直に記載すること。\n"
            "8. 現在株価との乖離率と総合評価\n"
            "9. 主要リスク・課題（上位5件）: Agent1から提供されるリスク情報（JSON形式）をパースし、各リスクの影響度（大・中・小）とその判定根拠を明確に含めること。\n"
            "10. 投資判断サマリー（参考意見として提供、最終判断はユーザーに委ねる旨を明記）\n\n"
            "⚠️ 各Agentから取得したデータに欠落がある場合は、MarkdownReadTool を使って `data/{ticker}/kessan_tansin.md` や `data/{ticker}/yuho.md` を直接読み込み、生データから数値を補完すること。\n"
            "⚠️ 数値計算は必ず DCFValuationTool および MultiplesValuationTool で実行すること。\n"
            "⚠️ LLM自身は絶対に数値計算を行わないこと。"
        ),
        expected_output=(
            "【緩和条件】情報が完全に揃わなくても構いません。取得できない場合は「該当データなし」と記載して次に進み、情報の深追いや無限ループを避けてください。\n"
            "Markdown形式の完全な企業価値分析レポート（3000〜5000文字程度）。\n"
            "以下の見出し構造を含むこと:\n"
            "# 企業価値分析レポート: {企業名} ({ticker})\n"
            "## 1. 企業概要・事業内容\n"
            "## 2. 業界構造・競争環境分析\n"
            "## 3. 最新決算財務指標（セグメント業績と配当方針）\n"
            "## 4. 複数年業績推移（配当・配当性向のトレンドを含む）\n"
            "## 5. 株価・需給状況\n"
            "## 6. 企業価値算定\n"
            "### 6-1. DCF法による企業価値\n"
            "   - 前提条件（WACC、成長率等）に加え、将来5年間の予測FCFテーブルを必ず含めること。\n"
            "   - 継続価値（TV）の計算結果と、それが全体の企業価値に占める割合（%）も記載すること。\n"
            "### 6-2. マルチプル法（同業種比較）\n"
            "### 6-3. 総合評価・現在株価との乖離\n"
            "## 7. リスク・課題\n"
            "   - リスク項目、影響度（大・中・小）、根拠・分析を含むテーブル形式で記述すること。\n"
            "## 8. 投資判断サマリー"
        ),
        agent=agent,
        context=[task1, task2, task3, task4, task5],  # 全Agentの結果を参照
    )
