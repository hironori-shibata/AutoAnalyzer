"""
Task3: ライバルリサーチ（情報収集フェーズ）
Agent3 (ライバルリサーチャー / Perplexity Sonar) が担当する。
Perplexityの検索能力を最大活用し、業界・競合・最新ニュースを先行収集する。
この結果はTask3a（競合定量データ収集）のインプットとなる。
"""
from datetime import date
from crewai import Task
from src.agents.agent3_rival import create_agent3


def create_task3(ticker: str, company_name: str = "") -> Task:
    agent = create_agent3()
    today = date.today().strftime("%Y年%m月%d日")
    return Task(
        description=(
            f"本日の日付: {today}\n"
            f"証券番号 {ticker} の企業について、Perplexityの検索機能をフル活用して以下の情報を収集してください。\n\n"
            "【重要】このタスクは後続の競合定量データ収集（Task3a）のインプットになります。\n"
            "競合他社を正確に特定し、社名・証券番号・その理由を明記してください。\n\n"
            "【Section 0: 対象企業の最新ニュース（今日〜12ヶ月）】\n"
            f"本日（{today}）時点での対象企業のニュース・イベントを積極的に収集せよ。\n"
            "【絶対禁止】財務指標・株価・PER/PBR等の定量データは他エージェント担当のため記載禁止。\n"
            "【収集対象（ニュース・イベントのみ）】:\n"
            "- 新製品・新技術・新サービスの発表・リリース（新分野への展開等を特に注目）\n"
            "- 受注・業務提携・資本提携・MOU締結の発表\n"
            "- M&A・買収・売却・合弁設立の発表\n"
            "- 新市場参入・海外展開・拠点開設・撤退の発表\n"
            "- 経営陣交代・組織再編・人事異動\n"
            "- 規制・認証・法的動向（許認可取得・行政処分・訴訟等）\n"
            "- 株主還元方針の変更（方針変更の事実）\n"
            "- 社会的・メディア的な話題（注目報道・テーマ株としての評価等）\n"
            "- 市場や投資家から注目される理由（例: 〇〇業界関連・半導体関連・EV関連など自分で調べたものであること）\n"
            "（直近3ヶ月のニュースは「★最新」と強調すること）\n\n"
            "【Section 1: 業界・市場環境の調査】\n"
            "以下を検索すること:\n"
            "1. 業界全体の市場規模・成長率・主要トレンド（需要拡大 or 縮小の方向性）\n"
            "2. 業界を取り巻くマクロ環境（技術変化・規制動向・地政学リスク等）\n"
            "3. 業界内で現在最も注目されているテーマ・キーワード\n\n"
            "【Section 2: 主要競合他社の特定】\n"
            "以下を調査すること:\n"
            "1. 対象企業の主要競合他社（国内・海外問わず5〜7社）を特定せよ\n"
            "2. 各競合他社について:\n"
            "   - 社名・国内企業の場合は証券番号（4桁）\n"
            "   - 競合と見なす理由（事業領域・製品・市場の重複）\n"
            "   - 直近12ヶ月の重要ニュース（業績・受注・提携・製品発表等）\n"
            "   - 強み・弱みの概要\n"
            "3. 対象企業の市場ポジション（シェア順位・主力製品・技術での地位）\n\n"
            "【Section 3: 競争優位性の予備分析】\n"
            "- 参入障壁: 業界特性上の障壁（技術的難易度・認証要件・資本集約性等）\n"
            "- スイッチングコスト: 顧客が競合製品へ乗り換える際の障壁\n"
            "- 対象企業の差別化要因（他社と何が違うか）\n\n"
            "【Section 4: カタリスト候補の列挙】\n"
            "株価が動く可能性があるトリガーを洗い出すこと:\n"
            "- ポジティブカタリスト候補（今後6〜18ヶ月）\n"
            "- ネガティブカタリスト候補（今後6〜18ヶ月）"
        ),
        expected_output=(
            f"証券番号 {ticker} の企業ニュース・業界・競合調査レポート（Markdown形式）。以下を含むこと:\n"
            "- Section 0: 対象企業の最新ニュース・イベント（直近3ヶ月は★最新マーク付き）\n"
            "  ※ 新分野への展開等の注目テーマがあれば必ず記載\n"
            "- Section 1: 業界・市場環境（市場規模・成長率・注目テーマ）\n"
            "- Section 2: 主要競合他社リスト（社名・証券番号・競合理由・最新動向・強み弱み）\n"
            "  ※ Task3aでの定量データ収集に使用するため、証券番号を可能な限り明記すること\n"
            "- Section 3: 競争優位性の予備分析（参入障壁・スイッチングコスト・差別化要因）\n"
            "- Section 4: カタリスト候補一覧（ポジティブ/ネガティブ各2件以上）"
        ),
        agent=agent,
    )
