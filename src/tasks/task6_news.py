"""
Task6: 最新業界ニュース・地政学リサーチタスク
対象企業のセクターを起点に、同心円状にニュースを収集する。
- 第1圏: 企業固有ニュース（直近12ヶ月）
- 第2圏: 直接競合・業界動向
- 第3圏: セクター特有のマクロ・地政学リスク
- 第4圏: 規制・政策・通商動向

【設計思想】
- 三菱重工のような軍需企業を分析する際に中東情勢が漏れるような問題を防ぐ
- セクター特定 → セクター固有のリスクカテゴリ展開 → 各カテゴリで最新情報収集
- Perplexity Sonarのウェブ検索内蔵を活用し、ツール不要で実行
"""
from datetime import date
from crewai import Task
from src.agents.agent6_news import create_agent6


def create_task6(ticker: str, task1: Task) -> Task:
    agent = create_agent6()
    today = date.today().strftime("%Y年%m月%d日")
    return Task(
        description=(
            f"本日の日付: {today}\n"
            f"証券番号 {ticker} の企業について、以下の手順で最新情報を収集し、"
            "構造化ニュースレポートを作成せよ。\n\n"
            "【STEP 0: セクター特定（必須・最初に行う）】\n"
            "Task1の有報リサーチ結果を参照し、以下を特定せよ:\n"
            "- 企業名\n"
            "- 主要事業セクター（防衛/宇宙/重工業・半導体/電子部品・自動車/輸送機器・\n"
            "  金融・エネルギー/資源・医薬品/ヘルスケア・不動産・小売/消費財 等）\n"
            "- そのセクターに固有のマクロリスクカテゴリを3〜5個列挙すること\n"
            "→ 以降のリサーチはこのリスクカテゴリに沿って実施すること。\n\n"
            f"【STEP 1: 企業固有ニュース（{today} 基準で直近12ヶ月）】\n"
            f"以下を調査せよ（{today} 基準で直近3ヶ月の動きは「★最新」と強調）:\n"
            "- 受注・契約・提携の重要発表\n"
            "- 業績上方・下方修正、ガイダンス変更\n"
            "- M&A・事業再編・資本政策（自社株買い・増資等）\n"
            "- 経営陣交代・不祥事・ガバナンス問題\n"
            "- 製品・技術の新発表・受賞・認定取得\n\n"
            f"【STEP 2: 業界・競合動向（{today} 基準で直近12ヶ月）】\n"
            "- 主要競合他社の重要ニュース（受注・提携・撤退等）\n"
            "- 業界全体の需要動向・市場規模変化\n"
            "- 業界再編（M&A・統合・新規参入）の動き\n"
            "- 技術トレンドの変化（既存製品を脅かす代替技術・破壊的イノベーション）\n\n"
            f"【STEP 3: セクター特有のマクロ・地政学リスク（{today} 基準で直近12ヶ月）】\n"
            "STEP 0で特定したリスクカテゴリごとに最新情報を収集せよ。\n"
            "以下は必ず含めること（セクターに応じて適切なものを選択）:\n"
            "- 防衛・宇宙・重工業の場合:\n"
            "  * 主要国（日本・米国・欧州・中国・中東）の国防予算の増減動向\n"
            "  * 現在進行中の地政学的紛争・緊張（中東・東アジア・欧州）の最新状況\n"
            "  * 武器輸出規制・防衛装備移転三原則の変化\n"
            "  * 自国防衛力強化（日本の防衛費増額・次世代戦闘機等）の調達計画\n"
            "- 半導体・電子部品の場合:\n"
            "  * 米国・EU・日本の対中輸出規制の最新動向\n"
            "  * AI関連需要（生成AI・データセンター向け）の現状\n"
            "  * 各国の半導体補助金政策（CHIPS法執行状況等）\n"
            "  * 地政学リスクの現状\n"
            "- 自動車・輸送機器の場合:\n"
            "  * EV市場の普及速度・主要国のEV補助金政策変化\n"
            "  * 米欧の通商関税（EV・部品への課税等）\n"
            "  * バッテリー資源サプライチェーンの価格・供給状況\n"
            "- その他セクターも同様に、業界特有の地政学・規制リスクを深掘りすること。\n\n"
            f"【STEP 4: 規制・政策・通商動向（{today} 基準で直近12ヶ月）】\n"
            "- 対象企業に直接影響する法改正・規制強化・緩和\n"
            "- 政府の産業政策・補助金・支援策の変化\n"
            "- 通商政策（関税・FTA・輸出規制・サプライチェーン政策）\n"
            "- 環境・ESG規制（CO2規制・人権デューデリジェンス等）\n\n"
            "【STEP 5: Agent7・Agent8・Agent9向けサマリー】\n"
            "収集した情報から、後続エージェントが最も参照すべき重要ポイントを以下で整理せよ:\n"
            "a) 株価ポジティブ影響が見込まれる最新情報（上位3件・根拠付き）\n"
            "b) 株価ネガティブ影響が見込まれる最新情報（上位3件・根拠付き）\n"
            "c) 現在の分析で最も見落とされやすい、しかし重大なリスク（1件・理由付き）\n"
            "   ※ このcが今回このエージェントを追加した核心目的。\n"
            "     例: 軍需企業なのに地政学的紛争の影響が評価されていない等\n\n"
            "【出力形式】\n"
            "Markdown形式で `## 業界・地政学ニュースレポート: {企業名} ({ticker})` から始めること。\n"
            "各STEPに見出しを付けること。\n"
            "情報ソース（ウェブ検索結果）は出典URLまたはソース名を括弧書きで付けること。\n"
            "直近3ヶ月以内の情報には「★最新」を付けること。\n"
            "文字数: 2000〜3500文字程度。"
        ),
        expected_output=(
            "## 業界・地政学ニュースレポート: {企業名} ({ticker})\n"
            "から始まるMarkdown形式の最新情報レポート（2000〜3500文字）。\n"
            "必須要素:\n"
            "- STEP 0: セクター特定 + セクター固有リスクカテゴリ3〜5個\n"
            "- STEP 1: 企業固有ニュース（★最新マーク付き）\n"
            "- STEP 2: 業界・競合動向\n"
            "- STEP 3: セクター特有のマクロ・地政学リスク（セクターに応じた深掘り）\n"
            "- STEP 4: 規制・政策・通商動向\n"
            "- STEP 5: Agent7/8/9向けサマリー（ポジティブ3件・ネガティブ3件・見落とされがちなリスク1件）"
        ),
        agent=agent,
        context=[task1],
    )
