"""
Task_Gemini: 対象企業ディープリサーチタスク
Google Gemini Flash (公式API / Google Search Grounding) が担当。
有報（Task1）で判明した企業の基本情報をベースに、
Google検索を活用して対象企業の最新情報を深堀りする。

【収集対象】
- 市場・投資家からの注目テーマ（AI・フィジカルAI・DX等）
- 最新のIR情報（決算説明会・投資家向け資料・中計進捗）
- 主力製品・技術の市場評価
- ESG・コーポレートガバナンス
- 機関投資家・アクティビスト動向
"""
from datetime import date
from crewai import Task
from src.agents.agent_gemini import create_agent_gemini


def create_task_gemini(ticker: str, task1: Task) -> Task:
    agent = create_agent_gemini()
    today = date.today().strftime("%Y年%m月%d日")
    return Task(
        description=(
            f"本日の日付: {today}\n"
            f"証券番号 {ticker} の企業について、Task1（有報リサーチ）で判明した企業名・事業内容をもとに、\n"
            "Google検索を活用して以下の最新情報を収集し、構造化レポートを作成してください。\n\n"
            "【重要】有報（Task1）は過去のスナップショットです。あなたは「今、市場が企業をどう見ているか」を収集します。\n"
            "財務数値（PER・PBR・売上等）は他エージェント担当のため収集不要です。\n\n"
            "【Section 1: 市場・投資家が注目するテーマ】\n"
            f"現在（{today}時点）、この企業に関して投資家・アナリストが最も注目しているテーマを収集せよ:\n"
            "- 「{企業名} フィジカルAI」「{企業名} AI 戦略 投資家」等で検索\n"
            "- 「{企業名} 注目銘柄 テーマ」「{企業名} アナリスト 評価 {today.split('年')[0]}」等で検索\n"
            "- 企業がどのような投資テーマ（AI・DX・EV・宇宙・防衛・フィジカルAI等）で語られているか\n"
            "- アナリストや機関投資家が評価するポイント・懸念点\n"
            "- 株式市場でこの銘柄が「テーマ株」として位置づけられている場合はその詳細\n"
            "（直近3ヶ月以内の情報は「★最新」と強調すること）\n\n"
            "【Section 2: IR・経営陣の最新メッセージ】\n"
            "- 「{企業名} 決算説明会」「{企業名} IR 投資家向け資料」等で検索\n"
            "- 直近の決算説明会・株主総会での経営陣の主要発言\n"
            "- 中期経営計画の進捗状況（目標値に対する達成度）\n"
            "- 事業ポートフォリオの変化・注力領域のシフト\n"
            "- 資本政策の方針（配当方針・自社株買い・M&A戦略）\n\n"
            "【Section 3: 製品・技術の市場評価】\n"
            "- 「{企業名} 新製品 技術 発表」「{企業名} 特許 R&D」等で検索\n"
            "- 主力製品・サービスの市場での競争力変化\n"
            "- 新製品・新技術の発表とその市場反応\n"
            "- 顧客・パートナー企業との関係性の変化（大型受注・提携・解約等）\n"
            "- 競合比較での技術優位性・劣位点の変化\n\n"
            "【Section 4: ESG・コーポレートガバナンス】\n"
            "- 「{企業名} ESG スコア 評価」「{企業名} ガバナンス」等で検索\n"
            "- 主要ESGインデックスへの採用・除外状況\n"
            "- 環境（CO2削減目標・再エネ移行）の取り組み状況\n"
            "- ガバナンス改革（取締役会構成・株主提案・アクティビスト動向）\n"
            "- 不祥事・コンプライアンス問題（あれば）\n\n"
            "【Section 5: 注目すべきリスク・機会（Gemini視点）】\n"
            "Sections 1〜4の情報収集を踏まえ、有報やマクロ分析では見落とされがちな\n"
            "企業固有のリスクと機会を3〜5点まとめること:\n"
            "- リスク: 収集した情報から読み取れる具体的なリスク（発生確率・株価インパクト付き）\n"
            "- 機会: 市場が十分に織り込んでいない可能性があるアップサイド要因\n"
            "- 特に「投資テーマとして注目されているが、実態評価が追いついていない点」があれば記載\n\n"
            "【Section 6: 株主優待】\n"
            "- 「{企業名} 株主優待」等で検索し、優待の有無・内容・条件を収集すること\n"
            "- 優待の種類（商品券・自社製品・食事券・QUOカード等）と金額換算目安\n"
            "- 優待取得に必要な最低保有株数・保有期間条件\n"
            "- 優待利回り（優待金額換算÷株価）の目安\n"
            "- 優待の変更・廃止リスクや直近の変更履歴（あれば）\n"
            "- 株主優待がない場合はその旨を明記し、代替として配当方針・自社株買いの状況を記載"
        ),
        expected_output=(
            f"証券番号 {ticker} の企業ディープリサーチレポート（Markdown形式）。以下を含むこと:\n"
            "- Section 1: 市場・投資家が注目するテーマ（AI・フィジカルAI等の注目テーマ、★最新マーク付き）\n"
            "- Section 2: IR・経営陣の最新メッセージ（決算説明会・中計進捗・資本政策）\n"
            "- Section 3: 製品・技術の市場評価（競争力変化・新製品・受注動向）\n"
            "- Section 4: ESG・コーポレートガバナンス（ESGスコア・ガバナンス改革・アクティビスト）\n"
            "- Section 5: 注目すべきリスク・機会（Gemini独自視点、発生確率・インパクト付き）\n"
            "- Section 6: 株主優待（優待内容・条件・優待利回り目安、優待なしの場合は配当・自社株買いの状況）\n"
            "【品質基準】Googleの検索インデックスを活用した最新情報（直近12ヶ月）を重視すること。\n"
            "「該当データなし」のみで終わるセクションは不可。必ず分析的な記述を含めること。"
        ),
        agent=agent,
        context=[task1],
    )
