"""
Task9: 最終投資判断タスク（独立アービトレーター担当）
Agent7（買いレポート）とAgent8（ショートレポート）の両方を受け取り、
確率加重期待リターンに基づく最終投資判断を下す。

【設計思想】
- どちらのレポートにも先入観を持たない第三者として設計
- 「正誤の分離」→「核心前提の特定」→「確率推定」→「期待株価計算」→「最終判断」の流れ
- DeepSeek R1の段階的推論能力を最大活用
- 期待リターン = Σ(シナリオ株価 × 確率) で機械的に判断を導出
"""
from crewai import Task
from src.agents.agent9_investor import create_agent9


def create_task9(ticker: str, task7: Task, task8: Task, task3b: Task) -> Task:
    agent = create_agent9()
    return Task(
        description=(
            f"あなたは独立した最終投資意思決定者である。\n"
            f"Agent7（買いレポート）とAgent8（ショートレポート）の両方を精読し、"
            f"証券番号 {ticker} に対する最終投資判断を下せ。\n"
            "⚠️ Task3b（競合分析レポート）も必ず参照せよ。"
            "競合他社との比較（バリュエーション・成長率・市場ポジション）をSTEP 1の「両者が一致している点」の検証材料として活用し、"
            "両レポートが競合比較を適切に行っているかも評価すること。\n\n"
            "【基本姿勢】\n"
            "- Agent7にもAgent8にも肩入れしない。どちらの結論も仮説として扱え。\n"
            "- 「どちらが正しいか」を判定するのではなく「核心前提の成立確率」を推定せよ。\n"
            "- 結論は確率加重期待リターンから機械的に導出せよ（印象・感覚での判断禁止）。\n"
            "- 各ステップで必ず根拠を示して段階的に論理を展開せよ（R1の推論能力を活用）。\n\n"
            "【必須構成・7ステップをすべて実行せよ】\n\n"
            "**STEP 1: 両レポートの正誤分離**\n"
            "以下の3区分で整理せよ:\n"
            "a) 両者が一致している点（バイアスを超えた事実に近い点）: 3点以上\n"
            "b) Agent7のみが主張していて根拠が弱い点: 2点以上（具体的数値を引用）\n"
            "c) Agent8のみが主張していて根拠が弱い点: 2点以上（具体的数値を引用）\n"
            "※ この分離の精度が最終判断の質を決める。形式的に済ませるな。\n\n"
            "**STEP 2: 核心前提の特定（1点のみ）**\n"
            "両レポートの対立の核心にある「最も重要な前提」を1つだけ選んで名指しせよ。\n"
            "選定基準: 「この前提の真偽によって最終株価が最も大きく変わるもの」\n"
            "例: 「主力セグメントの成長率が持続するか否か」\n"
            "    「WACCが4.7%で妥当か、それとも7〜9%が現実的か」\n"
            "その前提が「Agent7寄り」か「Agent8寄り」かを明示せよ。\n\n"
            "**STEP 3: 核心前提の成立確率推定**\n"
            "STEP 2で特定した前提について、以下の2つの確率を同時に推定せよ。\n"
            "  ① ブル確率（前提が『完全に』成立する確率）\n"
            "  ② ベース確率（前提が『一部』成立する確率）\n"
            "  ③ ベア確率 = 100% - ブル確率 - ベース確率（自動決定・明示すること）\n"
            "必ず『ベース確率 > ブル確率』を満たすこと。\n"
            "推定の根拠は以下から選択・組み合わせて示すこと:\n"
            "- 過去の類似事例（同業他社・過去サイクル）\n"
            "- マクロ環境（金利・為替・業界サイクルの現状）\n"
            "- 両レポートが共通して認めているデータ点\n"
            "- ベースレート（業界における成長継続率等の一般統計）\n"
            "最終的に「ブル確率: XX% / ベース確率: XX% / ベア確率: XX%」として数値で明示すること。\n"
            "【リスクコスト判定】推定の過程で以下のいずれかに該当する場合は、"
            "不確実性を『リスクコスト』として反映し、必ず『ベア確率 > ブル確率』になるよう確率を再調整すること:\n"
            "- 算出根拠が不明瞭（データ不足・両レポート間で矛盾するデータが存在する）\n"
            "- 今後12ヶ月の見通しが立てにくい（業界サイクルが読めない・マクロ不透明度が高い）\n"
            "再調整した場合はその旨と理由を1文で明示すること。\n\n"
            "**STEP 4: 3シナリオの構築**\n"
            "以下の3シナリオを構築せよ（それぞれ12ヶ月後の目標株価レンジを設定）:\n"
            "- ベースケース: STEP 3の確率推定を反映した最もありうる前提\n"
            "  → 両レポートで一致しているデータを軸に構築すること\n"
            "- ブルケース: 核心前提がAgent7寄りに成立した場合\n"
            "  → Agent7の楽観数値をそのまま使わず、現実的上限を設定すること\n"
            "- ベアケース: 核心前提がAgent8寄りに成立した場合\n"
            "  → Agent8の悲観数値をそのまま使わず、現実的下限を設定すること\n"
            "各シナリオに「そのシナリオが実現するために必要な条件」を1〜2点付けること。\n\n"
            "**STEP 5: 確率付与と期待株価計算**\n"
            "STEP 3で推定した『ブル確率 / ベース確率 / ベア確率』をそのままここで使用せよ（合計が100%であることを確認）。\n"
            "確率を変更してはならない。STEP 3とSTEP 5の確率は必ず一致させること。\n"
            "期待株価を以下の式で計算せよ:\n"
            "  期待株価 = (ブル株価中央値 × ブル確率)\n"
            "           + (ベース株価中央値 × ベース確率)\n"
            "           + (ベア株価中央値 × ベア確率)\n"
            "現在株価との比較から期待リターン（%）を算出すること。\n"
            "※ LLMが計算を行うことをこのステップに限り許可する\n\n"
            "**STEP 6: 最終投資判断（Buy / Hold / Sell）**\n"
            "以下のルールに従って最終判断を1つ選べ:\n"
            "- 期待リターン > +15%: Buy\n"
            "- 期待リターン +5%〜+15%: Hold（買い増し検討余地あり）\n"
            "- 期待リターン -5%〜+5%: Hold（中立）\n"
            "- 期待リターン < -5%: Sell\n"
            "判断を明記したうえで、以下を添えること:\n"
            "a) 定量根拠: 期待株価・期待リターンをそのまま引用\n"
            "b) 定性補足: 判断を支持する要因を1〜2点（数値なしの場合は不要）\n"
            "c) 信頼度: この判断への自己評価（High / Medium / Low）と理由\n"
            "   ※ データ不足・前提の不確実性が高い場合は正直にMedium/Lowを付けること\n\n"
            "**STEP 7: 判断が覆る条件（モニタリングトリガー）**\n"
            "以下の3種類のトリガーを各1点、具体的な数値付きで列挙せよ:\n"
            "a) 判断を上方修正するトリガー（Sell→Hold、Hold→Buy に動く条件）\n"
            "   例: 「主力セグメント売上が2四半期連続で前年比+15%超」\n"
            "b) 判断を下方修正するトリガー（Buy→Hold、Hold→Sell に動く条件）\n"
            "   例: 「WACCが実質7%超になる金利上昇が確認された場合」\n"
            "c) 判断の前提が完全に崩れるトリガー（即時再評価が必要な閾値）\n"
            "   例: 「競合から同等製品が市場投入され、主力製品のシェアが6ヶ月で10%超低下」\n\n"
            "【出力形式】\n"
            "Markdown形式で、`## 9. 最終投資判断（独立アービトレーター）` から始めること。\n"
            "各STEPに番号付き見出しを付けること。\n"
            "STEP 5の期待株価計算は数式を明示すること（例: 期待株価 = 価格(ベース)×確率 + 価格(ブル)×確率 + 価格(ベア)×確率 = ◯円）。\n"
            "STEP 6の最終判断は**太字**で強調すること。\n"
            "文字数: 2000〜3500文字程度。"
        ),
        expected_output=(
            "## 9. 最終投資判断（独立アービトレーター）\n"
            "から始まるMarkdown形式の最終判断レポート（2000〜3500文字）。\n"
            "必須要素:\n"
            "- STEP 1: 両レポート正誤分離（一致点3+、Agent7弱点2+、Agent8弱点2+）\n"
            "- STEP 2: 核心前提1点（両レポートの対立の根幹）\n"
            "- STEP 3: ブル確率/ベース確率/ベア確率（合計100%）と推定根拠・リスクコスト判定結果\n"
            "- STEP 4: 3シナリオ（ブル/ベース/ベア）と各シナリオの実現条件\n"
            "- STEP 5: STEP 3の確率をそのまま使用した期待株価計算式（数値明示）+ 期待リターン\n"
            "- STEP 6: **最終判断（Buy/Hold/Sell）** + 信頼度（High/Medium/Low）\n"
            "- STEP 7: モニタリングトリガー3点（上方修正・下方修正・即時再評価）"
        ),
        agent=agent,
        context=[task7, task8, task3b],
    )
