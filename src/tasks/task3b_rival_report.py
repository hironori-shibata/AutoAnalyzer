"""
Task3b: 競合リサーチ統合レポート作成タスク
Agent3b (競合分析レポート作成エージェント / DeepSeek Reasoner) が担当する。
Task3（情報収集）とTask3a（競合定量データ）の両結果を統合し、
最終的な競合分析レポートを作成する。
"""
from datetime import date
from crewai import Task
from src.agents.agent3_rival import create_agent3b


def create_task3b(ticker: str, task3: Task, task3a: Task) -> Task:
    agent = create_agent3b()
    today = date.today().strftime("%Y年%m月%d日")
    return Task(
        description=(
            f"本日の日付: {today}\n"
            f"証券番号 {ticker} の企業について、Task3（情報収集）とTask3a（競合定量データ）の\n"
            "両結果を統合し、包括的な競合分析レポートを作成してください。\n\n"
            "【重要ルール】\n"
            "- Task3で収集した定性情報（ニュース・業界トレンド・競争優位性の予備分析）を必ず活用すること\n"
            "- Task3aで収集した定量データ（PER・PBR等）をそのまま転記して活用すること\n"
            "- 情報が断片的でも分析を完成させること。「該当データなし」のみで終わることは禁止。\n"
            "- Task3で見つかった注目テーマを必ずレポートに反映すること\n"
            f"- 情報の鮮度を意識し、{today}時点での最新情報を優先すること。\n\n"
            "【Section 0: 対象企業の最新ニュース・注目テーマ】\n"
            "Task3のSection 0の情報をもとに、最重要ニュースを整理・深掘りすること:\n"
            "- 対象企業が市場・投資家から注目される理由（テーマ株としての評価含む）\n"
            "- 直近の重要イベント・発表（直近3ヶ月は「★最新」と強調）\n"
            "- 上記ニュースが株価や事業戦略に与えるインパクトの評価\n\n"
            "【Section 1: 競合比較分析】\n"
            "Task3aの定量データ + Task3の定性情報を統合して以下を作成:\n"
            "1. 競合他社比較テーブル（Task3aのPER・PBR・時価総額・配当利回り数値を使用）\n"
            "2. 各競合他社の最新動向・強み・弱み（Task3のSection 2を活用）\n"
            "3. 業界全体の市場規模・成長率・主要トレンド（Task3のSection 1を活用）\n\n"
            "【Section 2: 競争優位性の深堀り分析】\n"
            "Task3の予備分析をベースに、以下を詳細に分析すること:\n"
            "- 市場ポジション: 世界・国内シェア順位、主力製品・技術での地位\n"
            "- 参入障壁: 業界特性上の障壁（技術的難易度・認証要件・資本集約性等）\n"
            "- スイッチングコスト: 顧客が競合製品へ乗り換える際の障壁\n"
            "- 競合との差別化要因: Task3aのPER/PBR格差から読み取れる市場評価の違い\n"
            "※ 具体数値が見つからない項目は「{業界名}業界の一般的な水準として〜」と明記して補完せよ。\n\n"
            "【Section 3: カタリスト分析（今後6〜18ヶ月）】\n"
            "Task3のカタリスト候補を精査し、最終的なカタリスト一覧を作成:\n"
            "- ポジティブカタリスト（各2件以上、発生確率（高/中/低）・株価インパクト（大/中/小）付き）\n"
            "- ネガティブカタリスト（各2件以上、発生確率（高/中/低）・株価インパクト（大/中/小）付き）"
        ),
        expected_output=(
            f"証券番号 {ticker} の競合分析統合レポート（Markdown形式）。以下を含むこと:\n"
            "- Section 0: 対象企業の最新ニュース・注目テーマ（市場での評価・投資テーマを含む。直近3ヶ月は★最新マーク付き）\n"
            "- Section 1: 競合比較テーブル（Task3aのPER・PBR等数値）+ 各社最新動向・強み弱み\n"
            "- Section 2: 競争優位性の深堀り分析（市場ポジション・参入障壁・スイッチングコスト・差別化要因）\n"
            "  ※ 検索で数値が見つからない場合は業界知識・推論で補完し、その旨を明記すること\n"
            "- Section 3: カタリスト一覧（ポジティブ/ネガティブ各2件以上、発生確率・株価インパクト付き）\n"
            "【品質基準】「該当データなし」のみで終わるセクションは不可。必ず分析的な記述を含めること。"
        ),
        agent=agent,
        context=[task3, task3a],
    )
