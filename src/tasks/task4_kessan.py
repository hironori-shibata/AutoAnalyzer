"""
Task4: 最新決算短信リサーチタスク
Agent4 (最新決算短信リサーチャー) が担当する。
LLMに計算させず、必ずFinancialCalcToolを使うこと。
"""
from crewai import Task
from src.agents.agent4_kessan import create_agent4


def create_task4(ticker: str) -> Task:
    agent = create_agent4()
    return Task(
        description=(
            f"証券番号 {ticker} の最新決算短信について以下を実施してください:\n"
            "1. KessanFetcherTool を使って最新決算短信のMarkdownを取得\n"
            "2. Markdownから以下の財務数値を正確に抽出（単位に注意）:\n"
            "   - 当期純利益, 総資産, 自己資本（純資産）, 発行済株式数\n"
            "   - 売上高, 営業利益, 流動資産, 流動負債\n"
            "   - 有利子負債, 棚卸資産, 売上債権, 仕入債務, 売上原価\n"
            "   - 営業キャッシュフロー, 設備投資額（キャッシュフロー計算書等から抽出）\n"
            "   - 減価償却費（キャッシュフロー計算書の非資金費用として記載されることが多い）\n"
            "   - ⚠️ FCFの年間化推定（例: Q3累計を4/3倍するなど）は絶対に禁止です。最新が四半期の場合は、その時点の累計実績のみを抽出し、推定値は作成しないでください。\n"
            "3. 抽出した数値をFinancialCalcToolに渡して以下の指標を計算:\n"
            "   ROA / ROE / ROIC / EPS / 営業利益率 / 自己資本比率 / 流動比率 / D-Eレシオ / 総資産回転率 / CCC / デュポン分解要素 / FCF\n"
            "   - 減価償却費が取得できた場合は depreciation パラメータに渡し、EBITDA・EBITDA利益率も算出すること\n"
            "   - CCC（キャッシュコンバージョンサイクル）が算出できた場合は以下の深掘り分析を行うこと:\n"
            "     a) CCC = 売掛金回転日数(DSO) + 棚卸資産回転日数(DIO) - 買掛金回転日数(DPO)\n"
            "     b) どのコンポーネント（DSO/DIO/DPO）が長期化の主因かを特定する\n"
            "     c) 製造業の一般的な目安（CCC 60〜90日）と比較し、改善余地を評価する\n"
            "     d) CCC が過大な場合、それがROIC低下・FCF抑制の構造的要因かどうかを判定すること\n"
            "4. デュポン分解とROICを用いてROEの「質」と資本効率を深く分析すること:\n"
            "   - ROE = 売上高純利益率 × 総資産回転率 × 財務レバレッジ\n"
            "   - ROICと加重平均資本コスト（WACC目安5〜6%）を比較し、ROIC > WACCかどうかを確認\n"
            "   - ROEが低い/下落している場合、その根本原因を以下から特定すること:\n"
            "     a) 過剰資本（自己資本比率が高すぎて財務レバレッジ効果が薄い）\n"
            "     b) 低い資本回転率（大型設備投資の回収遅れ・長期CCC等）\n"
            "     c) 収益性の低下（利益率圧縮）\n"
            "5. セグメント別の業績（売上構成や利益率など）を抽出し、主力事業と成長事業を特定すること\n"
            "6. 今期の配当（実績・予想）および配当性向に関する記述を抽出すること\n\n"
            "⚠️ 数値計算は必ずFinancialCalcToolで行うこと。LLMで計算しないこと。\n"
            "⚠️ 【単位変換の厳守】FinancialCalcToolはすべてのmonetary値を『円（1円単位）』、株式数を『株（1株単位）』で受け取る。\n"
            "   - 決算短信が百万円単位の場合: 数値 × 1,000,000 してから渡すこと（例: 1,435,245百万円 → 1,435,245,000,000）\n"
            "   - 発行済株式数が千株単位の場合: 数値 × 1,000 してから渡すこと（例: 259,581千株 → 259,581,000）\n"
            "   - 単位変換を誤るとEPS・FCF等が1/1000または1/1000000になり、後続の評価計算が全壊する。"
        ),
        expected_output=(
            "【緩和条件】情報が完全に揃わなくても構いません。取得できない場合は「該当データなし」と記載して次に進み、情報の深追いや無限ループを避けてください。\n"
            "最新決算期の財務指標一覧を含むMarkdown形式のレポート。\n"
            "各指標の値と計算根拠（使用した生数値と単位）を明記すること。\n"
            "「ROEのデュポン分解とROIC分析」セクションを設け、収益性・効率性・財務レバレッジの3要素に加え、"
            "ROE低下/高止まりの根本原因（過剰資本・資本回転・収益性）を経営問題として分析すること。\n"
            "EBITDA（減価償却費が取得できた場合）、FCF、セグメント別業績のサマリー、配当・配当性向のサマリーも含めること。\n"
            "CCC分析セクションを設け、DSO/DIO/DPOの内訳・業界比較・ROIC/FCFへの影響評価を含めること。"
        ),
        agent=agent,
    )
